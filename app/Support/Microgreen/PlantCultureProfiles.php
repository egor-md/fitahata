    /**
     * @return list<array{section:string,label:string,meta?:string,value:string,bar_percent:int,bar_variant?:string}>
     */
    private static function nutritionBlocks(
        string $genitive,
        int $kcal,
        float $carbG,
        float $protG,
        float $fatG,
        array $vitaminRows,
        array $mineralRows,
        array $antioxidantRows,
    ): array {
        $e = PlantNutritionItem::SECTION_ENERGY;
        $p = PlantNutritionItem::SECTION_PROTEIN;
        $v = PlantNutritionItem::SECTION_VITAMINS;
        $m = PlantNutritionItem::SECTION_MINERALS;
        $a = PlantNutritionItem::SECTION_ANTIOXIDANTS;

        $energy = [
            ['section' => $e, 'label' => 'Калорийность', 'meta' => 'на 100 г', 'value' => $kcal.' ккал', 'bar_percent' => min(100, max(8, (int) round($kcal / 2))), 'bar_variant' => ''],
            ['section' => $e, 'label' => 'Углеводы', 'meta' => 'на 100 г', 'value' => number_format($carbG, 1, ',', '').' г', 'bar_percent' => min(100, (int) ($carbG * 8)), 'bar_variant' => 'mid'],
            ['section' => $e, 'label' => 'Жиры', 'meta' => 'на 100 г', 'value' => number_format($fatG, 1, ',', '').' г', 'bar_percent' => min(100, (int) ($fatG * 12)), 'bar_variant' => 'orange'],
        ];

        $protPct = min(100, (int) ($protG * 18));
        $protein = [
            ['section' => $p, 'label' => 'Белки (общие)', 'meta' => 'на 100 г', 'value' => number_format($protG, 1, ',', '').' г', 'bar_percent' => $protPct, 'bar_variant' => 'prot1'],
            ['section' => $p, 'label' => 'Незаменимые аминокислоты', 'meta' => 'на 100 г', 'value' => number_format($protG * 0.42, 2, ',', '').' г', 'bar_percent' => min(95, (int) ($protPct * 0.75)), 'bar_variant' => 'prot2'],
            ['section' => $p, 'label' => 'Аргинин', 'meta' => 'на 100 г', 'value' => number_format($protG * 0.06, 2, ',', '').' г', 'bar_percent' => min(90, (int) ($protPct * 0.35)), 'bar_variant' => 'prot3'],
        ];

        return array_merge($energy, $protein, $vitaminRows, $mineralRows, $antioxidantRows);
    }

    /** @param  list<array{0:string,1:string,2:string,3:int,4:string}>  $rows label, meta, value, bar%, variant */
    private static function vit(array $rows): array
    {
        $v = PlantNutritionItem::SECTION_VITAMINS;
        $out = [];
        foreach ($rows as $r) {
            $out[] = ['section' => $v, 'label' => $r[0], 'meta' => $r[1], 'value' => $r[2], 'bar_percent' => $r[3], 'bar_variant' => $r[4]];
        }

        return $out;
    }

    /** @param  list<array{0:string,1:string,2:string,3:int,4:string}>  $rows */
    private static function min(array $rows): array
    {
        $m = PlantNutritionItem::SECTION_MINERALS;
        $out = [];
        foreach ($rows as $r) {
            $out[] = ['section' => $m, 'label' => $r[0], 'meta' => $r[1], 'value' => $r[2], 'bar_percent' => $r[3], 'bar_variant' => $r[4]];
        }

        return $out;
    }

    /** @param  list<array{0:string,1:string,2:string,3:int,4:string}>  $rows */
    private static function ant(array $rows): array
